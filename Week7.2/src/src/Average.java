package src;

public class Average {

        public static void main(String[] args) {

            double number1 = 10.5;
            double number2 = 20.7;
            double number3 = 15.3;


            double average = (number1 + number2 + number3) / 3;

            // Print the average
            System.out.println("The average of " + number1 + ", " + number2 + ", and " + number3 + " is: " + average);
        }
    }


